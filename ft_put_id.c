#include "ft_printf.h"

int	ft_put_id(int x)
{
	size_t	len;

	len = 0;
	ft_putnbr_fd(x, 1);
	if (x < 0)
	{
		x *= (-1);
		len++;
	}
	while (x > 0)
	{
		len++;
		x /= 10;
	}
	return (len);
}
